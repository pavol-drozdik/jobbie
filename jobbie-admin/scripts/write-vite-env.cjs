/**
 * Writes app/.env.production.local for Vite production builds (CI + release).
 * VITE_* vars are baked at build time — GitHub secrets must be passed as env here,
 * not only stored in repository secrets without a workflow mapping.
 */
const fs = require('node:fs')
const path = require('node:path')

const root = path.join(__dirname, '..')
const outPath = path.join(root, 'app', '.env.production.local')

function firstEnv(...keys) {
  for (const key of keys) {
    const value = process.env[key]?.trim()
    if (value) return value
  }
  return ''
}

const viteAdminApiUrl =
  firstEnv('ADMIN_VITE_ADMIN_API_URL', 'VITE_ADMIN_API_URL') ||
  'http://127.0.0.1:3099'

const viteSupabaseUrl = firstEnv(
  'ADMIN_SUPABASE_URL',
  'VITE_SUPABASE_URL',
)
const viteSupabaseAnonKey = firstEnv(
  'ADMIN_SUPABASE_ANON_KEY',
  'VITE_SUPABASE_ANON_KEY',
)
const viteTurnstileSiteKey = firstEnv(
  'ADMIN_TURNSTILE_SITE_KEY',
  'VITE_TURNSTILE_SITE_KEY',
)

const lines = [
  '# Generated by scripts/write-vite-env.cjs — do not commit',
  `VITE_ADMIN_API_URL=${viteAdminApiUrl}`,
  `VITE_SUPABASE_URL=${viteSupabaseUrl}`,
  `VITE_SUPABASE_ANON_KEY=${viteSupabaseAnonKey}`,
  `VITE_TURNSTILE_SITE_KEY=${viteTurnstileSiteKey}`,
  '',
]

fs.writeFileSync(outPath, lines.join('\n'), 'utf8')
console.log('[vite-env] Wrote app/.env.production.local')
if (!viteSupabaseUrl || !viteSupabaseAnonKey) {
  console.warn(
    '[vite-env] VITE_SUPABASE_* empty — login via Nest API still works; Supabase client session helpers disabled.',
  )
}
