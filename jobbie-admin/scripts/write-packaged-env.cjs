/**
 * Writes build/api.env for electron-builder extraResources (resources/api.env).
 * CI: pass secrets as env vars (see .github/workflows/jobbie-admin-mac-release.yml).
 * Local: export the same vars, or use --from-example for unsigned test builds (no secrets).
 */
const fs = require('node:fs')
const path = require('node:path')

const root = path.join(__dirname, '..')
const outPath = path.join(root, 'build', 'api.env')
const examplePath = path.join(root, 'api', '.env.example')

const allowMissing = process.argv.includes('--from-example')

const REQUIRED = [
  'SUPABASE_URL',
  'SUPABASE_SERVICE_ROLE_KEY',
  'SUPABASE_ANON_KEY',
  'SUPABASE_JWT_SECRET',
  'AUDIT_CHAIN_SECRET',
]

const OPTIONAL_KEYS = [
  'ADMIN_API_PORT',
  'ADMIN_RECENT_LOGIN_MINUTES',
  'ADMIN_DEV_FULL_SCOPES',
  'SUPABASE_READ_URL',
  'SEARCH_ANALYTICS_SECRET',
  'POSTHOG_PERSONAL_API_KEY',
  'POSTHOG_PROJECT_ID',
  'POSTHOG_HOST',
  'GOOGLE_SERVICE_ACCOUNT_JSON',
  'GA4_PROPERTY_ID',
  'GSC_SITE_URL',
  'CLARITY_API_TOKEN',
  'PWA_PUBLIC_URL',
  'JOBBIE_PUBLIC_URL',
  'VPS_STAGING_SSH_HOST',
  'VPS_STAGING_SSH_USER',
  'VPS_STAGING_SSH_KEY_PATH',
  'VPS_STAGING_SSH_PRIVATE_KEY',
  'VPS_STAGING_HEALTH_URL',
  'VPS_STAGING_METRICS_URL',
  'VPS_STAGING_METRICS_BEARER_TOKEN',
  'VPS_STAGING_INFRA_HISTORY_PATH',
  'VPS_PRODUCTION_SSH_HOST',
  'VPS_PRODUCTION_SSH_USER',
  'VPS_PRODUCTION_SSH_KEY_PATH',
  'VPS_PRODUCTION_SSH_PRIVATE_KEY',
  'VPS_PRODUCTION_HEALTH_URL',
  'VPS_PRODUCTION_METRICS_URL',
  'VPS_PRODUCTION_METRICS_BEARER_TOKEN',
  'VPS_PRODUCTION_INFRA_HISTORY_PATH',
  'HOST_METRICS_SCRIPT_PATH',
  'INFRA_METRICS_HISTORY_PATH',
]

function escapeEnvValue(value) {
  const s = String(value)
  if (/[\s#"'\\]/.test(s)) {
    return `"${s.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`
  }
  return s
}

function writeLine(lines, key, value) {
  if (value == null || String(value).trim() === '') return
  lines.push(`${key}=${escapeEnvValue(String(value).trim())}`)
}

if (allowMissing) {
  if (!fs.existsSync(examplePath)) {
    console.error('Missing', examplePath)
    process.exit(1)
  }
  fs.mkdirSync(path.dirname(outPath), { recursive: true })
  fs.copyFileSync(examplePath, outPath)
  console.log(`Wrote template env (no secrets): ${outPath}`)
  process.exit(0)
}

const missing = REQUIRED.filter((key) => !process.env[key]?.trim())
if (missing.length > 0) {
  console.error(
    'Missing required env for packaged admin API:',
    missing.join(', '),
  )
  console.error(
    'Set GitHub Actions secrets or export locally before npm run env:packaged',
  )
  process.exit(1)
}

const lines = [
  '# Generated by scripts/write-packaged-env.cjs — bundled in desktop release',
  'ADMIN_API_PORT=3099',
  'ADMIN_RECENT_LOGIN_MINUTES=120',
]

for (const key of REQUIRED) {
  writeLine(lines, key, process.env[key])
}

for (const key of OPTIONAL_KEYS) {
  writeLine(lines, key, process.env[key])
}

if (!process.env.PWA_PUBLIC_URL?.trim() && !process.env.JOBBIE_PUBLIC_URL?.trim()) {
  lines.push('PWA_PUBLIC_URL=https://jobbie.sk')
}

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, `${lines.join('\n')}\n`, 'utf8')
console.log(`Wrote packaged env: ${outPath}`)
